// ------- helper: クライアント取得/作成 -------
function getOrCreateClient(int $cid, string $name): int {
    $db = getDB();
    $s = $db->prepare('SELECT id FROM sales_clients WHERE company_id=? AND client_name=? LIMIT 1');
    $s->execute([$cid, $name]);
    $id = $s->fetchColumn();
    if ($id) return (int)$id;
    $s2 = $db->prepare('INSERT INTO sales_clients (company_id,client_name,sort_order) VALUES (?,?,0)');
    $s2->execute([$cid, $name]);
    return (int)$db->lastInsertId();
}

// ------- helper: アライアンス取得/作成 -------
function getOrCreateAlliance(int $cid, string $name): int {
    $db = getDB();
    $s = $db->prepare('SELECT id FROM sales_alliances WHERE company_id=? AND alliance_name=? LIMIT 1');
    $s->execute([$cid, $name]);
    $id = $s->fetchColumn();
    if ($id) return (int)$id;
    $s2 = $db->prepare('INSERT INTO sales_alliances (company_id,alliance_name,alliance_type,sort_order) VALUES (?,?,?,0)');
    $s2->execute([$cid, $name, 'アライアンス']);
    return (int)$db->lastInsertId();
}

// ------- helper: 重複チェック -------
function caseExists(int $cid, string $type, int $year, int $month, int $clientId, string $workerName): bool {
    $db = getDB();
    $s = $db->prepare('SELECT COUNT(*) FROM sales_cases WHERE company_id=? AND case_type=? AND case_year=? AND case_month=? AND client_id=? AND worker_name=?');
    $s->execute([$cid, $type, $year, $month, $clientId, $workerName]);
    return (int)$s->fetchColumn() > 0;
}

// ------- helper: 値が "該当者なし" なら null -------
function nn(?string $v): ?string {
    if ($v === null || $v === '' || $v === '該当者なし') return null;
    return $v;
}

// ------- worker_type マッピング -------
function mapType(string $t): string {
    return match($t) {
        '外注' => '個人外注',
        default => $t,
    };
}

$log    = [];
$skip   = 0;
$ok     = 0;
$errors = [];

// ------- 案件登録 -------
function importCase(int $cid, array $d, array &$log, int &$ok, int &$skip, array &$errors): void {
    $clientId   = getOrCreateClient($cid, $d['client_name']);
    $allianceId = $d['alliance_name'] ? getOrCreateAlliance($cid, $d['alliance_name']) : null;

    foreach ($d['months'] as [$year, $month]) {
        if (caseExists($cid, $d['type'], $year, $month, $clientId, $d['worker_name'])) {
            $log[] = "SKIP [{$d['type']}] {$d['client_name']} / {$d['worker_name']} {$year}/{$month}";
            $skip++;
            continue;
        }
        $startDate = sprintf('%04d-%02d-01', $year, $month);
        $data = [
            'case_type'      => $d['type'],
            'client_id'      => $clientId,
            'sales_rep'      => $d['sales_rep'],
            'manager'        => $d['manager'],
            'recruiter'      => $d['recruiter'],
            'worker_type'    => $d['worker_type'],
            'alliance_id'    => $allianceId,
            'worker_id'      => null,
            'worker_name'    => $d['worker_name'],
            'store_brand_id' => null,
            'area_id'        => null,
            'store_name'     => $d['store_name'],
            'start_date'     => $d['type'] === 'event' ? $d['start_date'] : $startDate,
            'end_date'       => $d['type'] === 'event' ? ($d['end_date'] ?: null) : null,
            'carrier'        => $d['carrier'],
            'unit_price_in'  => $d['unit_price_in'],
            'unit_price_out' => $d['unit_price_out'],
            'days_worked'    => $d['days_worked'],
            'status'         => 'confirmed',
            'note'           => null,
        ];
        if ($d['type'] === 'regular') {
            $data['gross_profit_direct'] = $d['unit_price_in'] - $d['unit_price_out'];
        }
        try {
            createSalesCase($cid, $data);
            $log[] = "OK  [{$d['type']}] {$d['client_name']} / {$d['worker_name']} {$year}/{$month}";
            $ok++;
        } catch (Exception $e) {
            $errors[] = "ERR [{$d['type']}] {$d['client_name']} / {$d['worker_name']}: " . $e->getMessage();
        }
    }
}

